import numpy as np 

x = 1.0 #float x = 1
y = 2.0 #float y = 2

#trig stuff
print(np.sin(x))
print(np.cos(x))
print(np.tan(x))
print(np.arcsin(x))
print(np.arccos(x))
print(np.arctan(x))
print(np.arctan2(x,y)) #arctan(x/y)


#hyperbolic functions
print(np.sinh(x)) #sinh(x)
print(np.cosh(x)) #you get the idea
print(np.tanh(x))
print(np.arcsinh(x))
print(np.arccosh(x))
print(np.arctanh(x))